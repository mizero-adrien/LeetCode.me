class Solution:
    def countHillValley(self, nums):
        # 1. Remove consecutive duplicates
        arr = [nums[0]]
        for x in nums[1:]:
            if x != arr[-1]:
                arr.append(x)

        count = 0
        
        # 2. Check hills and valleys
        for i in range(1, len(arr) - 1):
            if arr[i] > arr[i-1] and arr[i] > arr[i+1]:  # hill
                count += 1
            elif arr[i] < arr[i-1] and arr[i] < arr[i+1]:  # valley
                count += 1
        
        return count
